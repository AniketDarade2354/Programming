
let x = 11;
let y = 21;

console.log(x += y);    // x = x + y

console.log(x -= y);    // x = x - y

console.log(x *= y);    // x = x * y

console.log(x /= y);    // x = x / y

console.log(x %= y);    // x = x % y

console.log(x **= y);    // x = x ** y

x = "Hello";
x += " World";

console.log(x);
